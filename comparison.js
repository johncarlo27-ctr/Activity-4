let firstFavNum = 8;
let secondFavNum = 27;

//Greater than
let greaterThan = firstFavNum > secondFavNum;
console.log(greaterThan);

//Less than
let lessThan = firstFavNum < secondFavNum;
console.log(lessThan);

//Greater than or equal
let greaterThanOrequal = firstFavNum >= secondFavNum;
console.log(greaterThanOrequal);

//Less than or equal to
let lessThanOrEqualTo = firstFavNum <= secondFavNum;
console.log(lessThanOrEqualTo);

//Strictly equality
let strictlyEquality = firstFavNum == secondFavNum;
console.log(strictlyEquality);

//Loose inequality
let looseInequality = firstFavNum !== secondFavNum;
console.log(looseInequality);

//AND
let and = firstFavNum && secondFavNum;
console.log(and);

//OR
let or = firstFavNum === secondFavNum;
console.log(or);

//NOT
let isCold = true;
let isHot = false;

console.log(!isCold);
console.log(!isHot);